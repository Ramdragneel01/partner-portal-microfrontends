/**
 * Returns module role requirements for route-level guarding.
 */
export function getRequiredRolesForModule(moduleKey: ModuleKey): readonly UserRole[] {
  return MODULE_ACCESS_POLICY[moduleKey].requiredRoles;
}

/**
 * Resolves a shell pathname into a module key when one exists.
 */
export function getModuleKeyForPath(path: string): ModuleKey | null {
  if (typeof path !== 'string' || path.trim() === '') {
    return null;
  }

  const pathname = path.trim().split(/[?#]/)[0];
  const root = `/${pathname.split('/').filter(Boolean)[0] ?? ''}`;

  return MODULE_KEY_BY_ROUTE_ROOT[root] ?? null;
}

/**
 * Checks whether a role can access a module.
 */
export function canRoleAccessModule(role: UserRole, moduleKey: ModuleKey): boolean {
  return MODULE_ACCESS_POLICY[moduleKey].requiredRoles.includes(role);
}

/**
 * Returns all modules visible to a given role.
 */
export function getAccessibleModules(role: UserRole): ModuleKey[] {
  return (Object.keys(MODULE_ACCESS_POLICY) as ModuleKey[]).filter((moduleKey) =>
    canRoleAccessModule(role, moduleKey)
  );
}

/**
 * Hook variant for component-level module access checks.
 */
export function useModuleAccess(moduleKey: ModuleKey): boolean {
  const { user } = useAuth();
  if (!user) {
    return false;
  }

  return canRoleAccessModule(user.role, moduleKey);
}
